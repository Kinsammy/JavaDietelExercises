package applicationPackages;

public class StudentMethod {
    public static int firstTotal(int firstStudentFirstSubject, int firstStudentSecondSubject, int firstStudentThirdSubject){
        return firstStudentFirstSubject + firstStudentSecondSubject + firstStudentThirdSubject;

    }


    public static int secondTotal(int  secondStudentFirstSubject, int secondStudentSecondSubject , int secondStudentThirdSubject){
        return secondStudentFirstSubject + secondStudentSecondSubject + secondStudentThirdSubject;
    }

    public static int thirdTotal(int  thirdStudentFirstSubject, int thirdStudentSecondSubject, int thirdStudentThirdSubject){
        return thirdStudentFirstSubject + thirdStudentSecondSubject + thirdStudentThirdSubject;
    }
}
